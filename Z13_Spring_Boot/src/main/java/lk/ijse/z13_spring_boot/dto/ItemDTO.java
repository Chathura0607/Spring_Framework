package lk.ijse.z13_spring_boot.dto;

public class ItemDTO {
    private int id;
    private String name;
    private double price;
    private double qty;

    public ItemDTO() {
    }

    public ItemDTO(int id, String name, double price, double qty) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.qty = qty;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public double getQty() {
        return qty;
    }

    public void setQty(double qty) {
        this.qty = qty;
    }
}
